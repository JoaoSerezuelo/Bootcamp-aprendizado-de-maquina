"""
as tuplas são imutáveis, ou seja, não podem ser alteradas
aceita elementos duplicados
"""
nomes=('Ana','João','Pedro','Ana')
print(nomes)
print('ana' in nomes)#verifica se o elemento ana está na tupla
print('Ana' in nomes)
print(nomes[0])
print(nomes[1:3])#mostra os elementos da posição do primeiro parametro até a posição do segundo parametro sem incluir o segundo parametro
